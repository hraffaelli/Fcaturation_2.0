    private void btnSuppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuppActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel)tblP.getModel();
        
        
        //Supprimer une ligne 
        if(tblP.getSelectedRowCount() == 1){
            
           String tblPu = tblModel.getValueAt(tblP.getSelectedRow(), 2).toString();
           String tblQu = tblModel.getValueAt(tblP.getSelectedRow(), 3).toString();
           TotalProduit = (Integer.parseInt(tblPu)) * (Integer.parseInt(tblQu));
           TotalPrix = TotalPrix - TotalProduit ;
           TotalQ = TotalQ - (Integer.parseInt(tblQu)); 
           
           txtPt.setText(Integer.toString(TotalPrix));
           txtQt.setText(Integer.toString(TotalQ));
           
           lesProduits.remove(tblP.getSelectedRow());           
           tblModel.removeRow(tblP.getSelectedRow());
           
           //Vider les champs
        txtNp.setText("");
        txtNa.setText("");
        txtPu.setText("");
        txtQu.setText("");
        }else{
            if(tblP.getRowCount()==0){
                JOptionPane.showMessageDialog(this,"Tableau vide");
            }else{
                JOptionPane.showMessageDialog(this,"Veuillez sélectionner un ligne");
            }
            
        }
    }//GEN-LAST:event_btnSuppActionPerformed

