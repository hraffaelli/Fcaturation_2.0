    private void btnAjouPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouPActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel)tblP.getModel();
        // Vérifie si les champ sont vide
        if(txtNp.getText().equals("")||txtNa.getText().equals("")||txtPu.getText().equals("")||txtQu.equals("")){
            JOptionPane.showMessageDialog(this,"Veuillez remplir les champs Entreprise !");
        }else{
            produit p = new produit(Integer.parseInt(txtNp.getText())
                    ,txtNa.getText()
                   ,Integer.parseInt(txtPu.getText())
                   ,Integer.parseInt(txtQu.getText()));        
            lesProduits.add(p);  
            tblModel.addRow(p.toTableau());    
            
          
          
            TotalProduit = (p.getQuantite()) * (p.getPrixUni());
            TotalPrix = TotalProduit + TotalPrix;
            TotalQ = TotalQ + p.getQuantite();    
           txtPt.setText(Integer.toString(TotalPrix));
           txtQt.setText(Integer.toString(TotalQ));
          
        //vider les champs
           txtNp.setText("");
           txtNa.setText("");
           txtPu.setText("");
           txtQu.setText("");      
        }
        
        
        
    }//GEN-LAST:event_btnAjouPActionPerformed

